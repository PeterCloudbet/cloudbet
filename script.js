function copyToClipboard() {
    const textToCopy = document.getElementById('result').innerText;
    navigator.clipboard.writeText(textToCopy).then(() => {
        alert('Text copied to clipboard');
    }).catch(err => {
        console.error('Error in copying text: ', err);
    });
}

